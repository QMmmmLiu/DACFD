x.1=rnorm(300,2,4)
x.2=rnorm(300,3,5)
x.1.cf=induce.cfe(.2,0,x.1)
x.2.cf=induce.cfe(0,.1,x.2)
mean(x.1);var(x.1)
mean(x.2);var(x.2)
rec.mean.var(x.1.cf)
rec.mean.var(x.2.cf)
t.test(x.1,x.2)
t.test(x.1.cf,x.2.cf)
lw.t.test(x.1.cf,x.2.cf,"a")
lw.t.test(x.1.cf,x.2.cf,"b")
